import Combine
import Core
@testable import Horizon

final class DomainServiceMock: DomainServiceProtocol {

    let result: Result<API, Error>

    init(result: Result<API, Error>) {
        self.result = result
    }

    func api() -> AnyPublisher<API, Error> {
        switch result {
        case .success(let api):
            return Just(api)
                .setFailureType(to: Error.self)
                .eraseToAnyPublisher()
        case .failure(let error):
            return Fail(error: error)
                .eraseToAnyPublisher()
        }
    }
}
